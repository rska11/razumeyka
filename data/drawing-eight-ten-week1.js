// Авторская первая неделя для ступени 8-10 лет.
// Это не автогенератор месяца: каждый урок собран как отдельная задача с собственной целью.

const VIEW = '0 0 300 210';

function step(hint, layer) {
  return { hint, layer };
}

function bg(fill = '#fffdf7') {
  return `<path d="M0 0 H300 V210 H0 Z" fill="${fill}"/>`;
}

function colorizeClosedShapes(svg, palette = ['#fb7185', '#60a5fa', '#facc15']) {
  let colorIndex = 0;
  const nextFill = () => palette[colorIndex++ % palette.length];
  const withFill = (tag) => tag.replace('fill="none"', `fill="${nextFill()}" fill-opacity="0.34"`);

  return svg
    .replace(/<path\b(?=[^>]*\bZ\b)[^>]*fill="none"[^>]*>/g, withFill)
    .replace(/<(circle|ellipse)\b[^>]*fill="none"[^>]*>/g, withFill);
}

function preview(steps, fill = '#f8fafc', stroke = '#1f2937', palette) {
  const lineArt = steps.map((s) => s.layer).join('');
  return `${bg(fill)}${colorizeClosedShapes(lineArt, palette)}`
    .replaceAll('stroke="currentColor"', `stroke="${stroke}"`)
    .replaceAll('fill="currentColor"', `fill="${stroke}"`);
}

function levelByIndex(index) {
  if (index < 3) return 1;
  if (index < 7) return 2;
  return 3;
}

function lesson(day, index, data) {
  const level = levelByIndex(index);
  const order = day * 100 + index + 1;
  return {
    slug: `fw810-d${String(day).padStart(2, '0')}-${String(index + 1).padStart(2, '0')}-${data.slug}`,
    ageBand: '8-10',
    level,
    title: data.title,
    theme: data.theme,
    chapter: data.chapter,
    skill: data.skill,
    kind: data.kind ?? (level === 3 ? 'combo' : level === 2 ? 'focus' : 'draw'),
    order,
    free: true,
    cover: data.cover,
    viewBox: VIEW,
    coloredViewBox: VIEW,
    canvas: 'landscape',
    palette: data.palette,
    intro: data.intro,
    steps: data.steps,
    storyStageLabel: level === 1 ? 'наблюдение' : level === 2 ? 'приём' : 'сцена',
    storyPrompt: data.storyPrompt,
    storyMissions: data.storyMissions,
    colorHint: data.colorHint,
    finishIdea: data.finishIdea,
    parentNote: data.parentNote,
    coloredPreview: data.coloredPreview ?? preview(data.steps, data.previewBg, data.previewStroke, data.palette),
  };
}

