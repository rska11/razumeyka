export function getMentalLesson(slug) {
  return mentalLessons.find((l) => l.slug === slug);
}

// Уроки, сгруппированные по учебным дням: [{ n, title, lessons: [...] }].
export function mentalLessonsByDay() {
  const byDay = new Map();
  for (const lesson of mentalLessons) {
    if (!byDay.has(lesson.day)) byDay.set(lesson.day, []);
    byDay.get(lesson.day).push(lesson);
  }
  return [...byDay.entries()]
    .sort((a, b) => a[0] - b[0])
    .map(([n, lessons]) => ({
      n,
      title: mentalDays.find((d) => d.n === n)?.title ?? `День ${n}`,
      lessons: lessons.sort((a, b) => (a.order ?? 0) - (b.order ?? 0)),
    }));
}

