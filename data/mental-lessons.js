// Самостоятельные уроки ментальной арифметики (self-study, как /risovanie).
// Ребёнок считает на ИНТЕРАКТИВНОМ абакусе прямо на экране (настоящие счёты не нужны),
// результат в конце урока показывает родителю. Часть уроков бесплатна,
// остальные — по подписке (freemium). Оценок и аттестации нет — лицензия не требуется.
//
// ─────────────────────────────────────────────────────────────────────────────
// КОНТРАКТ УРОКА (для генерации уроков — читать перед добавлением!)
//
// Урок = { slug, day, order, title, kind, skill, free, cover:{emoji}, intro,
//          steps:[...], parentNote }
//   day    — учебный день 1..20 (месяц = 20 дней, как в рисовании).
//            День 0 — «Ступень 0: готовлюсь считать»: необязательная разминка для
//            самых маленьких (4–6), кто ещё неуверенно узнаёт цифры. Уроки дня 0
//            делаем free:true и БЕЗ абакуса: считаем предметы (count), узнаём
//            цифры (flash view:'digits'), teach с картинкой-эмодзи (picture).
//   order  — порядок внутри дня;
//   kind   — ярлык карточки: 'learn' | 'practice' | 'flash' | 'solve' | 'game';
//   skill  — короткая строка «что тренируем» (видна на карточке);
//   free   — true = играбельно без подписки (обычно весь день 1 и весь день 0).
//
// Шаг (steps[i]) — атом движка. Поле type выбирает экран плеера:
//   { type:'teach', text, show?, picture? }  — наставник объясняет; абакус (не кликается)
//                                             показывает число show (0 = пустой). Если show
//                                             не задан, вместо абакуса показывается picture —
//                                             строка эмодзи крупно (для дня 0); без picture —
//                                             только текст.
//   { type:'count', emoji, n, text?, options? }
//                                           — «посчитай предметы»: на экране n штук emoji,
//                                             ответ кнопками (день 0, счёт без абакуса; n ≤ 10).
//   { type:'set', target, text }            — «набери число»: интерактивный абакус,
//                                             проверка автоматически при совпадении.
//   { type:'read', value, text?, options? } — «какое число на абакусе?»: показ числа value
//                                             + кнопки-варианты ответа.
//   { type:'flash', cards, speedMs?, view?, text?, options? }
//                                           — флеш-карты: числа cards мелькают по одному
//                                             (view: 'abacus' | 'digits', по умолчанию
//                                             'abacus'; speedMs — показ одной карты,
//                                             по умолчанию 1400). Вопрос: одна карта —
//                                             «какое число мелькнуло», несколько — сумма.
//   { type:'solve', chain, useAbacus?, text?, options? }
//                                           — пример-цепочка: chain=[2,1,-1] значит
//                                             «2 + 1 − 1». Ответ — сумма. useAbacus:true
//                                             даёт ребёнку интерактивный абакус-помощник.
//
// options (для read/flash/solve/count) можно НЕ писать — плеер сам сгенерирует 3 варианта
// вокруг правильного ответа. Пишите options только когда важны конкретные «ловушки».
//
// Темп по возрасту: посетитель выбирает ступень (mentalAgeBands). Она НЕ меняет список
// уроков — только скорость флеш-карт (paceFactor) и рекомендации. Уроки пишем одни для всех.
//
// Озвучка: у каждого шага может быть say (текст для диктора). mp3 кладутся в
// public/audio/mental/{slug}/{intro|step-N|parent}.mp3 — конвенция та же, что у рисования.
//
// Числа: месяц 1 работает в пределах 0–9 (один разряд абакуса). Плеер сам определяет
// число разрядов по максимальному числу шага — двузначные появятся в месяце 2.
// ─────────────────────────────────────────────────────────────────────────────

